from collections import Counter

def simulate(fish, days=80):
    fish = Counter(fish)
    for day in range(days):
        # Storing the number of spawning fishes
        new_fish = fish[0]
        # Adjusting the days until spawn
        fish = Counter({k-1: fish[k] for k in range(1, 9)})
        # Adding the spawning fishes to the 6 day group
        fish[6] += new_fish
        # Adding the spawned fishes
        fish[8] = new_fish
    return sum(fish.values())

